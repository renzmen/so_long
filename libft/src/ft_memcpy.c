#include "../includes/libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*dp;
	const unsigned char	*sp;

	if (!dest && !src)
		return (0);
	dp = dest;
	sp = src;
	while (n--)
		*dp++ = *sp++;
	return (dest);
}
